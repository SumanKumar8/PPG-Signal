def estimate_hr_peaks(signal, fs, min_bpm=40, max_bpm=200):
    min_distance = int(fs * 60 / max_bpm)

    peaks, properties = find_peaks(
        signal,
        distance=min_distance,
        prominence=np.std(signal) * 0.4
    )

    peak_times = peaks / fs

    if len(peak_times) < 2:
        return np.nan, peaks

    ibi = np.diff(peak_times)
    bpm = 60 / np.mean(ibi)

    return bpm, peaks

