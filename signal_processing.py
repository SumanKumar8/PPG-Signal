def estimate_hr_fft(signal, fs, low=0.5, high=4.0):
    n = len(signal)
    freqs = rfftfreq(n, d=1/fs)
    spectrum = np.abs(rfft(signal))

    mask = (freqs >= low) & (freqs <= high)

    peak_freq = freqs[mask][np.argmax(spectrum[mask])]
    bpm = peak_freq * 60

    return bpm, freqs, spectrum

