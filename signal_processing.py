def butter_bandpass_filter(signal, fs, lowcut=0.5, highcut=4.0, order=4):
    b, a = butter(
        order,
        [lowcut, highcut],
        btype="bandpass",
        fs=fs
    )
    filtered = filtfilt(b, a, signal)
    return filtered, b, a

