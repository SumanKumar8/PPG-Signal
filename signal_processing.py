def estimate_hr_welch(signal, fs, low=0.5, high=4.0, nperseg=256):
    freqs, psd = welch(signal, fs=fs, nperseg=nperseg)

    mask = (freqs >= low) & (freqs <= high)

    peak_freq = freqs[mask][np.argmax(psd[mask])]
    bpm = peak_freq * 60

    return bpm, freqs, psd

