def nlms_filter(primary_signal, reference_signal, filter_order=8, mu=0.1, eps=1e-6):
    """
    primary_signal: noisy PPG = clean PPG + motion artifact
    reference_signal: accelerometer-derived motion reference

    returns:
    cleaned_signal: primary_signal - estimated_motion
    estimated_noise: estimated motion artifact
    """

    n_samples = len(primary_signal)

    w = np.zeros(filter_order)
    estimated_noise = np.zeros(n_samples)
    cleaned_signal = np.zeros(n_samples)

    for n in range(filter_order, n_samples):
        x = reference_signal[n-filter_order:n][::-1]

        y = np.dot(w, x)
        e = primary_signal[n] - y

        norm_factor = np.dot(x, x) + eps
        w = w + (mu / norm_factor) * e * x

        estimated_noise[n] = y
        cleaned_signal[n] = e

    return cleaned_signal, estimated_noise

