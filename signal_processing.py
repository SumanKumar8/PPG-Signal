def ppg_to_bpm_pipeline(ppg_signal, fs=25):
    # Step 1: detrend
    x = detrend(ppg_signal)

    # Step 2: bandpass filter
    x_filtered, _, _ = butter_bandpass_filter(
        x,
        fs=fs,
        lowcut=0.5,
        highcut=4.0,
        order=4
    )

    # Step 3: FFT estimate
    bpm_fft, _, _ = estimate_hr_fft(x_filtered, fs)

    # Step 4: Welch estimate
    bpm_welch, _, _ = estimate_hr_welch(x_filtered, fs)

    # Step 5: peak estimate
    bpm_peaks, peaks = estimate_hr_peaks(x_filtered, fs)

    return {
        "filtered_signal": x_filtered,
        "bpm_fft": bpm_fft,
        "bpm_welch": bpm_welch,
        "bpm_peaks": bpm_peaks,
        "peaks": peaks
    }

